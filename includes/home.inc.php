        <div class="panel">
            <h5 class="subheader text-center">1. Import trees</h5>
            <p>CompPhy's data is organized in projects in which you can import up to two collections of trees in Newick format. Collections can contain a large number of trees. Trees can have branch lengths and supports.</p>
        </div>
    </div>
    <div class="small-3 columns">
        <div class="panel">
            <h5 class="subheader text-center">2. Invite collaborators</h5>
            <p>You can create public or private projects and invite people to join them. Each project is stored online and can be accessed from anywhere with any device having Internet facility.</p>
        </div>
    </div>
    <div class="small-3 columns">
        <div class="panel">
            <h5 class="subheader text-center">3. Work on the project</h5>
            <p>CompPhy implements various tools to help you display and compare trees: coloring, rerooting, (automatic) swapping, restricting to common taxa, computing supertrees, etc.</p>
        </div>
    </div>
    <div class="small-3 columns">
        <div class="panel callout">
            <h5 class="subheader text-center">4. See changes live</h5>
            <p>Edit your trees and everything is immediately updated in your collaborators' browsers: every member of the project connected at this time sees the edits <strong>live</strong>, as you perform them.</p>
        </div>
    </div>
</div>
